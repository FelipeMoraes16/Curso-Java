/**
 * Objetivo: Praticar operadores de atribuição.
 */

public class OperadoresAtribuicaoExercicio {

	public static void main(String[] args) {

		int numero = 5;

		numero += 3; // 8
		numero -= 2; // 6
		numero *= 4; // 24
		numero /= 2; // 12

		System.out.println("Valor final de numero: " + numero);
	}
}